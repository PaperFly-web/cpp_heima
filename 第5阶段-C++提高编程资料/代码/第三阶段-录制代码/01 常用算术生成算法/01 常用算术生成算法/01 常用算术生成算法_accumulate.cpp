#include<iostream>
using namespace std;
#include <vector>

#include <numeric>

//常用算术生成算法   区间类的元素累加
void test01()
{
	vector<int>v;

	for (int i = 0; i <= 100; i++)
	{
		v.push_back(i);
	}
	//参数3  起始累加值 accumulate
	int total = accumulate(v.begin(), v.end(), 0);

	cout << "total = " << total << endl;
}

int main() {

	test01();

	system("pause");

	return 0;
}